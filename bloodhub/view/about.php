		<h3>What is this all about? </h3>
		<p>
		Project Blood Hub is an innovative approach
        to address global health. We provide immediate access
        to blood donors all over Bangladesh, 24 hours a day, 7 days a week!
        Blood Hub is one of several community organizations working
        together as a network that respond to disasters 
		or emergency situations in an efficient manner.
		</p>
		
		<h3>What we do? </h3>
		<p>
		The ultimate goal of this project is to
        provide an easy-to-use, easy-to-access,
        efficient, and reliable way to get life-saving blood,
        free of cost. Blood Hub works with network partners
        to connect blood donors and recipients through an automated
        SMS (text messaging) service or our mobile application.
        Our network of volunteer blood donors are
        ready to help save lives any time, any place.
		</p>
		
		<h3> How it works?</h3>
		<p>
		Our automated system works efficiently whenever someone needs a
		blood transfusion. Simply post a blood a request
		within our system, either on this website or by 
		downloading our mobile app. As soon as a new blood
		request is raised, it is routed among our local volunteer
		blood donors. We know time matters! So we keep you updated with 
		real-time notifications sent directly to you via SMS (text message) 
		or the installed mobile app. Instead of having to search all over for 
		a blood donor in an emergency situation, you can spend your time consoling the patient.
		We keep you updated at each step of the request process, from when a volunteer has been
		found to when blood is on its way.
		
		</p>
		
		
		<h3> Don't have access to internet?</h3>
		<p>
		In case, someone is unable to use the mobile application
		or the website or has not enough knowledge to understand
		how they can find a blood donor in an emergency, 
		we have an automated SMS (text message) service. 
		All you need to do is send a text message to 8655,
		"blood need <bloodgroup> in <your-city>".
		It does not need to be in english, you can
		write in any language you want. Our automated 
		systems are smart enough to understand everything
		you write and will interact with you and help you
		find a blood donor within seconds if not minutes.
		
		</p>
		
		
